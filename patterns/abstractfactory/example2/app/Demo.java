package patterns.abstractfactory.example2.app;

import patterns.abstractfactory.example2.factories.GUIFactory;
import patterns.abstractfactory.example2.factories.MacOSFactory;
import patterns.abstractfactory.example2.factories.WindowsFactory;

public class Demo {

    /**
     * Application picks the factory type and creates it in run time (usually at
     * initialization stage), depending on the configuration or environment
     * variables.
     */
    private static Application configureApplication() {
        Application app;
        GUIFactory factory;
        String osName = System.getProperty("os.name").toLowerCase();
        if (osName.contains("mac")) {
            factory = new MacOSFactory();
        } else {
            factory = new WindowsFactory();
        }
        app = new Application(factory);
        return app;
    }

    public static void main(String[] args) {
        Application app = configureApplication();
        app.paint();
    }
}